# دالة رد البوت
def رد_البوت(سؤال):
    سؤال = شيل_تشكيل(سؤال.strip())
    if سؤال in ردود:
        return random.choice(ردود[سؤال])
    for كلمة, مفتاح in كلمات_مفتاحية.items():
        if كلمة in سؤال:
            if مفتاح in ردود:
                return random.choice(ردود[مفتاح])
    return "مش فاهم اللي قلته، ممكن تعيد بصيغة تانية؟"

# مسار API
@app.route('/chat', methods=['POST'])
def chat():
    data = request.json
    message = data.get('message', '')
    response = رد_البوت(message)
    return jsonify({'reply': response})

